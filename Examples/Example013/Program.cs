void PrintImage(int[,] img)
{
    for (int i = 0; i < img.GetLength(0); i++)
    {
        for (int j = 0; j < img.GetLength(1); j++)
        {
            if (img[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void PaintImage(int x, int y)
{
    if (pic[x, y] == 0)
    {
        pic[x, y] = 1;
        PaintImage(x - 1, y);
        PaintImage(x, y - 1);
        PaintImage(x + 1, y);
        PaintImage(x, y + 1);
    }
}

